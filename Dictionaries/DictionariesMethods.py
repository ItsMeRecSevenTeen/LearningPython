thisdict = {
    "brand" : "Ford",
    "Model" : "Mustang",
    "year" : "1964"
}
thisdict.clear()    #Removes or empties all the elements from the dictionary
thisdict.copy()     #Returns a copy of the dictionary
thisdict.fromkeys() #Return a dictionary with the specified keys and values
thisdict.get()      #Returns the value of the specified key
thisdict.items()    #Returns a list containing a tuple for each key value pair
thisdict.keys()     #Return a list containing the dictionary's keys
thisdict.pop()      #Removes the element with the specified key
thisdict.popitem()  #Removes the last inserted key-value pair
thisdict.setdefault()   #Return the value of the specified key. If the key does not exist, inserts the key with the
                        #Specified value
thisdict.update()   #Updates the dictionary with the specified key-value pairs
thisdict.values()   #Returns a list of all the values in the dictionary