#Upper Case
a = " HelloH, World! "
print(a.upper())#returns the string in upper case
print(a.lower())#returns the string in lower case
print(a.strip())#returns the string without whitespaces from the beginning or the end
print(a.replace("H", "J"))#Replaces the first matched character with the second argument
print(a.split(","))#returns [' HelloH', 'World! '], also, returns a list by the separator character