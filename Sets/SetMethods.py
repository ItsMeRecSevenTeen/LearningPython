thisset = {1, 2, 3}
thisset.add()                           #Adds an element to the set
thisset.clear()                         #Removes all the items from the set
thisset.copy()                          #Returns a copy of the set
thisset.difference()                    #Returns a set containing the difference between two or more sets
thisset.difference_update()             #Removes the items in this set that are included in another specified set
thisset.discard()                       #Remove the specified item
thisset.intersection()                  #Returns a set, that is the intersection of other sets
thisset.intersection_update()           #Removes the in this set that are not present in other specified set or sets
thisset.isdisjoint()                    #Returns whether (if) two sets have a intersection or not
thisset.issubset()                      #Returns whether (if) another set contains this set or not
thisset.issuperset()                    #Returns whether (if) this set contains another set or not
thisset.pop()                           #Removes randomly an element from the set
thisset.remove()                        #Removes the specified element
thisset.symmetric_difference()          #Returns a set with the symmetric differences of two sets
thisset.symmetric_difference_update()   #Inserts the symmetric differences from this set and another set
thisset.union()                         #Return a set containing the union of sets
thisset.update()                        #Update the set with the union of this set and others