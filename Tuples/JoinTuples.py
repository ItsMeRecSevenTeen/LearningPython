#Joining two Tuples
tuple1 = ("a", "b", "c")
tuple2 = (1, 2, 3)
tuple3 = tuple1 + tuple2    #We can use the addition operator to concatenate two or more than 2 tuples
print(tuple3)
#Multiply Tuples
fruits = ("Apple", "Banana", "Cherry")
mytuple = fruits * 2    #Multiplies the tuple fruits 2 times and assigns to other tuple
print(mytuple)